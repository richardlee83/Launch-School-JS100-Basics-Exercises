/*
  What is an excellent destination when looking for JavaScript documentation?
*/

/*
  Answer
  - MDN Docs

  Solution
  - The JavaScript pages on MDN are a valuable resource that you should reference 
    frequently as you learn JavaScript. 
  - They provide both tutorials and a reference of JavaScript's built-in objects and 
    their properties and methods, operators, and more. 
  - You can also find information about browser compatibility and specifications.

  Discussion
  - Take some time to familiarize yourself with MDN; you will need to reference it 
    often. 
  - In particular, make sure you know where the search bar is, as it is often the 
    fastest entry point - whether you're looking for Array methods, examples of using 
    the break statement, an explanation of first-class functions, or any other 
    information.
*/