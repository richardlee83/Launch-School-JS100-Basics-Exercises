/*
  What happens if we take the array ['fish', 'and', 'chips'] and try to access 
  the element at index position 10? 

  Try this in your JavaScript console.
*/


// the value undefined is returned
let array = ['fish', 'and', 'chips'];
console.log(array[10]);                 // undefined

